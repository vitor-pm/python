
num1 = int(input("Entre com o primeiro numero: "))
num2 = int(input("Entre com o segundo numero: "))

soma = num1 + num2
subtracao = num1 - num2
divisao = num1 / num2
multiplicacao = num1 * num2
resto = num1 % num2

print('Soma: {}\nSubtração: {}\nDivisão: {}\nMultiplicação: {}\nResto: {}'
      .format(soma, subtracao, divisao, multiplicacao, resto))
